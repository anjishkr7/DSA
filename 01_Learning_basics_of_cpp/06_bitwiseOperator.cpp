#include<iostream>
using namespace std;
int main(){
  //bitwise And(&):-
  cout<<(5&4)<<endl;

  //bitwise OR(|):-
  cout<<(5|4)<<endl;

  //bitwise NOT(~):-
  cout<<(~1)<<endl;//number ke sare bits ko flip kr deta hai

  //Bitwise Left shift operator:-
  cout<<(5<<2)<<endl;//(num*2^k)

  //Bitwise Right shif:-
  cout<<(5>>2)<<endl;//(num/2^k)

  //Bitwise XOR(^):-same cheez->0 diff 1
  cout<<(5^5)<<endl;


  return 0;
}
